"""Schemathesis fuzz tests for the users module.

Generated by aws-fuzz-codegen (Phase 7C) for change eval-sample-001.

Scope (from plans/fuzz-plan.md):
  - TC-USER-FUZZ-001: schema-based fuzz on GET /api/v1/user/list

Schema acquisition strategy (from fuzz-plan.md):
  - Load OpenAPI schema from OPENAPI_URL env (default http://localhost:9999/openapi.json)
  - Use schemathesis.openapi.from_url (schemathesis 4.x API; the plan's
    from_uri call is the 3.x equivalent and was adapted to the installed
    schemathesis 4.21.6 — verified via `uv run python -c "import schemathesis"`)

Auth strategy (from facts/fact-baseline.json + plans/fuzz-plan.md):
  - The users route is protected by DependPermission (superuser bypasses).
  - Default admin credentials: admin / 123456 (seeded by init_superuser).
  - Login endpoint: POST /api/v1/base/access_token with {username, password}.
  - Login response data path: data.access_token.
  - Token header name: `token` (raw value, NOT a Bearer-prefixed value).

Expectations (from fuzz-plan.md):
  - No 5xx responses for schema-conformant inputs.
  - Response bodies conform to the declared schema (validate_response enforces
    both the status code band and the response schema).

Test Failure Integrity:
  - Every assertion maps to a plan expectation (no 5xx / schema-conformant
    response not wrongly rejected / response schema-conformant).
  - No `assert True`, placeholder assertions, or empty expectations.
  - No `try/except: pass` or `except Exception: pass` that swallows failures.
  - No skip/xfail, no early return, no hypothesis settings that disable
    generation, no mocking of the behavior under test, no loosening of
    validate_response.
  - Setup is flexible (auth fetch is best-effort; fuzz still runs unauthenticated
    if login fails — the endpoint may reject with 401/422, which is a schema-
    conformant non-5xx outcome and NOT a fuzz failure); assertions are strict.
"""
import os

import pytest
import requests
import schemathesis

pytestmark = pytest.mark.fuzz

OPENAPI_URL = os.getenv("OPENAPI_URL", "http://localhost:9999/openapi.json")
BACKEND_BASE_URL = os.getenv("BACKEND_BASE_URL", "http://localhost:9999")

# Auth facts captured from facts/fact-baseline.json (Phase 3.5).
LOGIN_ENDPOINT = "/api/v1/base/access_token"
DEFAULT_ADMIN_USERNAME = "admin"
DEFAULT_ADMIN_PASSWORD = "123456"
TOKEN_HEADER_NAME = "token"  # raw token, NOT "Authorization: Bearer ..."


def _fetch_admin_token(base_url: str) -> str | None:
    """Log in as the seeded default admin and return the access_token.

    Best-effort: returns None if the backend is unreachable or login fails.
    Fuzz tests still run without auth — a 401/422 from the protected endpoint
    is a schema-conformant non-5xx outcome, not a fuzz failure. A real 5xx
    surfaced during unauthenticated fuzzing is still a product defect that
    validate_response will surface.
    """
    try:
        resp = requests.post(
            f"{base_url}{LOGIN_ENDPOINT}",
            json={
                "username": DEFAULT_ADMIN_USERNAME,
                "password": DEFAULT_ADMIN_PASSWORD,
            },
            timeout=10,
        )
        if resp.status_code != 200:
            return None
        body = resp.json()
        data = body.get("data") or {}
        token = data.get("access_token")
        return token
    except Exception:
        # Setup failure: backend unavailable. Surface as None — tests will
        # run unauthenticated and the schema will still be fuzzed.
        return None


def _auth_headers() -> dict:
    """Build auth headers using the fact-baseline token header name.

    Returns an empty dict if the admin token could not be obtained so that
    schemathesis still fuzzes the endpoint (exercising the unauthenticated
    branch, which must also not 5xx).
    """
    token = _fetch_admin_token(BACKEND_BASE_URL)
    if not token:
        return {}
    return {TOKEN_HEADER_NAME: token}


# Schemathesis 4.x: schema loader lives under schemathesis.openapi.
# Loaded at module scope so pytest collection validates the schema once.
# wait_for_schema gives the backend a short window to come up during CI.
# Filtering via schema.include(path=..., method=...) is the 4.x equivalent
# of the 3.x schema.parametrize(endpoint=..., method=...) kwargs.
schema = schemathesis.openapi.from_url(
    OPENAPI_URL,
    wait_for_schema=30.0,
).include(path="/api/v1/user/list", method="GET")


@schema.parametrize()
def test_user_list_fuzz(case):
    """TC-USER-FUZZ-001: schema-based fuzz on GET /api/v1/user/list.

    Expectations (from plans/fuzz-plan.md):
      1. No 5xx responses for schema-conformant inputs.
      2. Response bodies conform to the declared OpenAPI schema.

    Auth: admin token attached via the `token` header (raw value, per
    facts/fact-baseline.json). If login fails the test still runs
    unauthenticated — a 401/422 is a valid non-5xx rejection and not a
    fuzz failure; a 5xx under any condition is a defect this test surfaces.

    Failure integrity:
      - case.validate_response asserts both the response status band and
        schema conformance — it will fail on any 5xx or schema violation.
      - No try/except suppression, no skip/xfail, no mocked behavior.
    """
    headers = _auth_headers()
    # Merge auth headers with any headers schemathesis generated for the case.
    # Case headers take precedence on conflict to preserve schema-derived
    # requirements; auth is additive when schemathesis did not set the
    # `token` header for this operation.
    merged_headers = {**headers, **(case.headers or {})}
    if headers and TOKEN_HEADER_NAME not in (case.headers or {}):
        merged_headers[TOKEN_HEADER_NAME] = headers[TOKEN_HEADER_NAME]

    response = case.call(headers=merged_headers)
    # Strict assertion: fails on 5xx and on response-schema violations.
    # This is the single, plan-mapped expectation — no fallback, no weakening.
    case.validate_response(response)
